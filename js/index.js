window.onload = function()
{
    // lazy loading of images
    //$("img").unveil();

    $('img').unveil(200, function() {
        $(this).load(function() {
            this.style.opacity = 1;
        });
    });

    // rotate menu chevron on mobile site
    // bug: this can be pressed faster than the menu toggles, and does not sync state with it...
    var toggle1 = true;
    $('#menu-button').click(function() {
        if (toggle1) {
            $('#menu-chevron').rotate({
                // from 0 deg to 90
                duration:400,
                angle: 0,
                animateTo:180
            });
        } else {
            $('#menu-chevron').rotate({
                // and vice versa
                duration:400,
                angle: 180,
                animateTo:0
            });
        }
        toggle1 = !toggle1;
    });
}






